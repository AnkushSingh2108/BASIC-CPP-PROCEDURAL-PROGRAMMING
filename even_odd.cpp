/*
Q) WAP to check whether a givrn number is ODD or EVEN
*/

#include<iostream>
using namespace std;

int main(int argc, char const *argv[])
{
    cout<<"Even Odd Program"<<endl;
    cout<<"Enter a integer number"<<endl;
    int n;
    cin>>n;
    if (n%2 == 0)
    {
        cout<<n<<"is even"<<endl;

    }
    else;
    {
        cout<<n<<"is odd"<<endl;
    }
    return 0;
}
